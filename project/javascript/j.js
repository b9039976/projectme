     
var t = document.getElementById("logo");
t.addEventListener(onmouseover , bigImg);
function bigImg(x) 
{
    x.style.height = "200px";
    x.style.width = "200x";
  }
  t.addEventListener(onmouseout , normalImg)
  function normalImg(x)
   {
    x.style.height = "90px";
    x.style.width = "90px";
  }
 

